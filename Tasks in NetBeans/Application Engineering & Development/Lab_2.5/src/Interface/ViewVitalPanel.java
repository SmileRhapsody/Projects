    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedrow = tbVitalSigns.getSelectedRow();
        if (selectedrow >= 0){
            VitalSigns vs = (VitalSigns)tbVitalSigns.getValueAt(selectedrow, 0);
            vsh.deleteVitals(vs);
            populateTable();
            resetTextField();
            JOptionPane.showMessageDialog(null, "Vital Sign has been deleted");
        }else{
            JOptionPane.showMessageDialog(null, "Please select any row");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
        // TODO add your handling code here:
        int selectedrow = tbVitalSigns.getSelectedRow();
        if (selectedrow >= 0){
            VitalSigns vs = (VitalSigns)tbVitalSigns.getValueAt(selectedrow, 0);
            bpText.setText(String.valueOf(vs.getBloodPressure()));
            temText.setText(String.valueOf(vs.getTemperature()));
            pulText.setText(String.valueOf(vs.getPulse()));
            dateText.setText(String.valueOf(vs.getDate()));
        }else{
            JOptionPane.showMessageDialog(null, "Please select any row");
        }
    }//GEN-LAST:event_btnViewDetailsActionPerformed


