    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        if(temText.getText().isEmpty()||bpText.getText().isEmpty()||pulText.getText().isEmpty()||dateText.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Fill in The Blank");
        }else if(MainFrame.toDouble(temText.getText()) && MainFrame.toDouble(bpText.getText()) && MainFrame.toInt(pulText.getText())){
            try{
                VitalSigns v = vsh.addVitals();
                double temperature = Double.parseDouble(temText.getText());
                double bp = Double.parseDouble(bpText.getText());
                int pulse = Integer.parseInt(pulText.getText());
                String date = dateText.getText();
                //也可以在前面先声明，再this.v=vsh.addVitals(); 就可以直接用v
                v.setTemperature(temperature);
                v.setBloodPressure(bp);
                v.setPulse(pulse);
                v.setDate(date);
                JOptionPane.showMessageDialog(null, "Vital Signs Added Succesfully");
                temText.setText("");
                bpText.setText("");
                pulText.setText("");
                dateText.setText("");
                }catch(NumberFormatException e){}
            }else{
            MainFrame.showDouble(temText.getText());
            MainFrame.showDouble(bpText.getText());
            MainFrame.showInt(pulText.getText());
        }
    }//GEN-LAST:event_jButton1ActionPerformed


