    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable.getSelectedRow();
        if (selectedRow >= 0){
            Product product = (Product)jTable.getValueAt(selectedRow, 0);
            ViewPanel vp = new ViewPanel(rightPanel, product);
            CardLayout layout = (CardLayout) rightPanel.getLayout();
            rightPanel.add(vp);
            layout.next(rightPanel);
        }else{
            JOptionPane.showMessageDialog(null, "Please select a row", "Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable.getSelectedRow();
        if (selectedRow >= 0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the account details", 
                    "Warning", dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION){
                Product product = (Product)jTable.getValueAt(selectedRow, 0);
                proDir.deleteProduct(product);
                populate();
            } 
        }else{
            JOptionPane.showMessageDialog(null, "Please select a row", "Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Product result = proDir.searchProduct(search.getText());
        if (result == null){
            JOptionPane.showMessageDialog(null, "Product Id does not exist", "Information",JOptionPane.INFORMATION_MESSAGE);
        }else{
            ViewPanel vp = new ViewPanel(rightPanel, result);
            CardLayout layout = (CardLayout) rightPanel.getLayout();
            rightPanel.add(vp);
            layout.next(rightPanel);
        }
    }//GEN-LAST:event_jButton3ActionPerformed


