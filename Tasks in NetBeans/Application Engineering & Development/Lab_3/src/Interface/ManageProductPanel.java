    public void populate(){
        DefaultTableModel dtm = (DefaultTableModel)jTable.getModel();
        dtm.setRowCount(0);
        for(Product pro:proDir.getProductlist()){
            Object[] row = new Object[2];
            row[0] = pro;
            row[1] = pro.getName();
            dtm.addRow(row);
        }
    }

