    public void populate(){
        DefaultTableModel dtm = (DefaultTableModel) flightTable.getModel();
        dtm.setRowCount(0);
        for(Flight flight : airplane.getFlightdirectory().getFlightList()){
            Object[] row = new Object[3];
            row[0] = flight;
            row[1] = flight.getfLocation();
            row[2] = flight.gettLocation();
            dtm.addRow(row);
        }
        airlinerName.setText(airplane.getAirlinerName());
        name.setText(airplane.getAirplaneName());
        size.setText(Double.toString(airplane.getAirplaneSize()));
    }

    private void backAction(){
        CardSequenceJPanel.remove(this);
        Component[] componentArray = CardSequenceJPanel.getComponents();
        Component component = componentArray[componentArray.length-1];
        ManageAirplanesJPanel ma = (ManageAirplanesJPanel) component;
        ma.populate();
        CardLayout layout = (CardLayout) CardSequenceJPanel.getLayout();
        layout.show(CardSequenceJPanel, "manageairplanes");
    }
