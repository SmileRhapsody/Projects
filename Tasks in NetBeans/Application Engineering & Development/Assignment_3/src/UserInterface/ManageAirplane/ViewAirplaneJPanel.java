    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        if (name.getText().isEmpty() || size.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Please fill in the blank","Warning",JOptionPane.WARNING_MESSAGE);
        }else if(toDouble(size.getText())){
            airplane.setAirplaneName(name.getText());
            airplane.setAirplaneSize(Double.parseDouble(size.getText()));
            JOptionPane.showMessageDialog(null,"Airplane created successfully");
        }else{
            showInt(size.getText());
        }
        populate();
        name.setEnabled(false);
        size.setEnabled(false);
        saveBtn.setEnabled(false);
        saveBtn.setEnabled(false);
        updateBtn.setEnabled(true);
    }//GEN-LAST:event_saveBtnActionPerformed


