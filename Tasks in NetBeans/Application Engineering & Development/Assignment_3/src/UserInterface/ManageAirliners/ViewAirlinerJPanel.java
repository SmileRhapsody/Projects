     public void populate(){
        DefaultTableModel dtm = (DefaultTableModel)airplaneTable.getModel();
        dtm.setRowCount(0);
        for(Airplane airplane:airplanedirectory.getAirplaneList()){
            Object[] row = new Object[3];
            row[0] = airplane;
            row[1] = airplane.getAirlinerName();
            row[2] = airplane.getAirplaneSize();
            dtm.addRow(row);
        }
        name.setText(airliner.getAirlinerName());
        address.setText(airliner.getAddress());
        conNum.setText(Integer.toString(airliner.getContaceNum()));
        
    }
     
    private void backAction(){
        CardSequenceJPanel.remove(this);
        Component[] componentArray = CardSequenceJPanel.getComponents();
        Component component = componentArray[componentArray.length - 1];
        ManageAirlinersJPanel manageairlinersJPanel = (ManageAirlinersJPanel) component;
        manageairlinersJPanel.populate();
        CardLayout layout = (CardLayout) CardSequenceJPanel.getLayout();
        layout.previous(CardSequenceJPanel);
    }

