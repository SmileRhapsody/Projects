    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
         if (name.getText().isEmpty() || address.getText().isEmpty() || conNum.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Please fill in the blank","Warning",JOptionPane.WARNING_MESSAGE);
        }else if(toInt(conNum.getText())){
                airliner.setAirlinerName(name.getText());
                airliner.setAddress(address.getText());
                airliner.setContaceNum(Integer.parseInt(conNum.getText()));
                JOptionPane.showMessageDialog(null, "Data updated successfully");
        }else{
            showInt(conNum.getText());
        }
        populate();
        name.setEnabled(false);
        address.setEnabled(false);
        conNum.setEnabled(false);
        saveBtn.setEnabled(false);
        updateBtn.setEnabled(true);
    }//GEN-LAST:event_saveBtnActionPerformed


