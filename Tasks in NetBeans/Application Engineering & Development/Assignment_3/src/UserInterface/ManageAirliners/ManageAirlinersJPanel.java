    public void populate(){
        DefaultTableModel dtm = (DefaultTableModel)airlinerTable.getModel();
        dtm.setRowCount(0);
        for(Airliner airliner:airlinerdirectory.getAirlinerList()){
            Object[] row = new Object[1];
            row[0] = airliner;
            dtm.addRow(row);
        }
    }
