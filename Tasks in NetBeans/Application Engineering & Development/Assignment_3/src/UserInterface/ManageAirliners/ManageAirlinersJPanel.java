    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int row = airlinerTable.getSelectedRow();
        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the airliner", 
                    "Warning", dialogButton);
        if (dialogResult == JOptionPane.YES_OPTION){
            Airliner airliner = (Airliner)airlinerTable.getValueAt(row, 0);
            airlinerdirectory.remove(airliner);
            populate();
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

