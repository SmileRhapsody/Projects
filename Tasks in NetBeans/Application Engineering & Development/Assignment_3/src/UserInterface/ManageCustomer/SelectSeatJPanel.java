    public void populate(){
        columnComboBox.removeAllItems();
        ArrayList<String> column = new ArrayList<>();
        for(Seat seat : seatdirectory.getSeatList()){
            if (!column.contains(seat.getColumn())){
                column.add(seat.getColumn());
            }
        }
        for (String s : column){
            columnComboBox.addItem(s);
        }
    }
    
