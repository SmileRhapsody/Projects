    private void bookBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookBtnActionPerformed
        // TODO add your handling code here:
        if(rowComboBox.getSelectedItem() == null){
            JOptionPane.showMessageDialog(null, "Please select Column first");
        }
        for (Seat seat : seatdirectory.getSeatList()){
            if (seat.getColumn().equals(columnComboBox.getSelectedItem())  && seat.getRow().equals(rowComboBox.getSelectedItem()) ){
                seatdirectory.getSeatList().remove(seat);
                JOptionPane.showMessageDialog(null, "Book successfully!");
                b = false;
                break;
            }
        }
        bookBtn.setEnabled(false);
        
    }//GEN-LAST:event_bookBtnActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        rowComboBox.removeAllItems();
        ArrayList<String> row = new ArrayList<>();
        for (Seat seat : seatdirectory.getSeatList()){
            if (seat.getColumn().equals(columnComboBox.getSelectedItem())){
                row.add(seat.getRow());
            }
        }
        for (String s : row){
            rowComboBox.addItem(s);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

