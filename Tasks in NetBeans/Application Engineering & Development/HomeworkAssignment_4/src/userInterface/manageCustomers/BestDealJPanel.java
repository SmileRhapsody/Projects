    public void populateTable(String source, String destination, Date flightDate, String timeOfDay) {

        List<Integer> pricesList = new ArrayList<>();
        SimpleDateFormat parser = new SimpleDateFormat("MM-dd-yyyy");
        ArrayList<FlightSchedule> flightScheduleArrayList = travelAgency.getMasterScheduleDirectory().getMasterFlightSchedules();
        for (FlightSchedule ad : flightScheduleArrayList) {

            if (ad.getSource().equalsIgnoreCase(source)
                    && ad.getDestination().equalsIgnoreCase(destination)
                    && parser.format(ad.getFlightDate()).equalsIgnoreCase(txtDepartureDate.getText())
                    && ad.getTimeOfDay().equals(timeOfDay)) {
                pricesList.add(ad.getPrice());

            }

        }
        getMinimumPriceFlightAndDisplay(pricesList);
    }

    private void getMinimumPriceFlightAndDisplay(List<Integer> priceList) {
        DefaultTableModel dtm = (DefaultTableModel) tblCheapFlights.getModel();
        dtm.setRowCount(0);
        int minPrice = Collections.min(priceList);
        for (FlightSchedule ad : travelAgency.getMasterScheduleDirectory().getMasterFlightSchedules()) {
            if (ad.getPrice() == minPrice) {
                Object row[] = new Object[6];
                row[0] = ad;
                row[1] = ad.getAirlinerName();
                row[2] = ad.getSource();
                row[3] = ad.getDestination();
                row[4] = ad.getFlightDate();
                row[5] = ad.getPrice();
                dtm.addRow(row);

            }
        }

       
    }

