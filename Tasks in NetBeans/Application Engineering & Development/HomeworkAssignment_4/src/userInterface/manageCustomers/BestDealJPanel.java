    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat parser = new SimpleDateFormat("MM-dd-yyyy");
        Date flightDate = null;
        String source = txtSource.getText();
        String destination = txtDestination.getText();
        String timeOfDay = txtTimeOfDay.getText();

        if (!source.isEmpty() && !destination.isEmpty() && !timeOfDay.isEmpty()) {
            try {
                flightDate = parser.parse(txtDepartureDate.getText());
                populateTable(source, destination, flightDate, timeOfDay);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Please enter date in correct format MM-dd-yyyy");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please enter values for empty fields");
        }

    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = tblCheapFlights.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from table first", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            FlightSchedule flight = (FlightSchedule) tblCheapFlights.getValueAt(selectedRow, 0);
            BookingCustomerJPanel bookCustomerJPanel = new BookingCustomerJPanel(userProcessContainer,flight,travelAgency);
            userProcessContainer.add("BookCustomerJPanel", bookCustomerJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnSelectActionPerformed

