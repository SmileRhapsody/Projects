    private boolean checkFlightsDirectory(String airlinerId) {
        String airlinerIdFromList;
        for (FlightSchedule ad : masterScheduleDirectory.getMasterFlightSchedules()) {
            airlinerIdFromList = ad.getFlightId();

            if (airlinerIdFromList.equals(airlinerId)) {
                return true;
            }
        }
        return false;
    }

    public boolean containsString(String s) {

        if (s != null && !s.isEmpty()) {
            for (char c : s.toCharArray()) {
                if (Character.isDigit(c)) {
                    return false;
                }
            }
        }

        return true;
    }

    public boolean validateSourceOrDestinationName(boolean isBankName) {
        if (!isBankName) {
            JOptionPane.showMessageDialog(null, "Source or destination contains number");
            return false;
        }
        return true;
    }


