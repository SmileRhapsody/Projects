    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat parser = new SimpleDateFormat("MM-dd-yyyy hh:mm");
        String flightId = txtFlightId.getText();
        int price = 0;
        String source = txtSource.getText();
        String destination = txtDestination.getText();
        String timeOfDay = txtTimeOfDay.getText();
        Date departureTime;
        boolean isSource = containsString(source);
        boolean isDestination = containsString(destination);
        boolean isTimeOfDay = containsString(timeOfDay);
        String airlinerName=txtAirlinerName.getText();

        try {
            try {
                price = Integer.parseInt(txtPrice.getText());
                departureTime = parser.parse(txtDepartureTime.getText());
                if (!flightId.isEmpty() && price != 0
                        && !source.isEmpty() && !destination.isEmpty() && !airlinerName.isEmpty()) {
                    if (!checkFlightsDirectory(flightId)) {

                        if (validateSourceOrDestinationName(isSource) && validateSourceOrDestinationName(isDestination)) {
                            FlightSchedule flights = masterScheduleDirectory.addFlights();
                            flights.setFlightId(flightId);
                            flights.setDestination(destination);
                            flights.setSource(source);
                            flights.setFlightDate(departureTime);
                            flights.setTimeOfDay(timeOfDay);
                            flights.setPrice(price);
                            flights.setAirlinerName(airlinerName);
                            JOptionPane.showMessageDialog(null, "Flight successfully created!");
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Flight ID already exist,please enter some another ID");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Please enter values for empty fields");
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Please proper integer value for price");

            } catch (ParseException exception) {
                JOptionPane.showMessageDialog(null, "Please enter the date for  departure time in the correct format:"
                        + " mm-dd-yyyy hh:mm");
            }
        } catch (Exception exception) {
            JOptionPane.showMessageDialog(null, "Flight cannot be created!");

        }
    }//GEN-LAST:event_btnCreateActionPerformed

