    private void btnViewAirlinerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewAirlinerActionPerformed
        // TODO add your handling code here
        int selectedRow = tblChildAirlinerList.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from table first", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            Airliner airliner = (Airliner) tblChildAirlinerList.getValueAt(selectedRow, 0);
            ViewAirlinerJPanel viewAirlinerPanel = new ViewAirlinerJPanel(userProcessContainer, airliner, travelAgency.getMasterScheduleDirectory());
            userProcessContainer.add("ViewAirlinerJPanel", viewAirlinerPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnViewAirlinerActionPerformed

