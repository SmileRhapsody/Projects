    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblAirlinersList.getModel();
        dtm.setRowCount(0);
        displayValuesInTable(dtm);
    }

    private void displayValuesInTable(DefaultTableModel dtm) {
        for (Airliner ad : travelAgency.getAirlinerDirectory().getAirlinersDirectory()) {
            Object row[] = new Object[4];
            row[0] = ad;
            row[1] = ad.getAirlinerName();
            row[2] = ad.getManufacturerName();
            row[3] = ad.getAddress();
            dtm.addRow(row);

        }
    }

    public void populateSecondaryTable(String airline) {
        DefaultTableModel dtm = (DefaultTableModel) tblChildAirlinerList.getModel();
        dtm.setRowCount(0);
        for (Airliner ad : travelAgency.getAirlinerDirectory().getAirlinersDirectory()) {

            if (ad.getAirlinerName().equals(airline)) {
                Object row[] = new Object[4];
                row[0] = ad;
                row[1] = ad.getAddress();
                row[2] = ad.getManufacturerName();
                dtm.addRow(row);
            }

        }

    }

